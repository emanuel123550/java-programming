public class PrintNumbers {
    public static void main(String[] args) {
        
        int i = 0;
        System.out.println("Numbers from 0 to 10:");
        while (i <= 10) {
            System.out.println(i);
            i++;
        }


        int j = 10;
        System.out.println("Numbers from 10 to 0:");
        while (j >= 0) {
            System.out.println(j);
            j--;
        }
    }
}

